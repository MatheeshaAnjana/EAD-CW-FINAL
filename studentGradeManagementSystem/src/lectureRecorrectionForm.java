    private void loadRecorrectionData() {
    // Define column names
    String[] columnNames = {"ID", "Student ID", "Subject ID", "Comment", "Status"};

    // Create a new table model with column names
    DefaultTableModel model = new DefaultTableModel(columnNames, 0);
    pnlRecorrectionData.setModel(model); // Set model to your JTable

    try {
        Connection con = DatabaseConnection.getConnection();
        String sql = "SELECT recorrection_id, student_id, subject_id, comment, status FROM recorrection";
        PreparedStatement stmt = con.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();

        while (rs.next()) {
            Object[] row = {
                rs.getInt("recorrection_id"),
                rs.getInt("student_id"),
                rs.getInt("subject_id"),
                rs.getString("comment"),
                rs.getString("status")
            };
            model.addRow(row);
        }

    } catch (Exception e) {
        e.printStackTrace();
    }
}

