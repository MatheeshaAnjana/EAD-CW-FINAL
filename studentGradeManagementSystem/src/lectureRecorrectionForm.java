    private void btnChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeActionPerformed
      if (txtRecorrectionId.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a recorrection ID.");
        return;
    }

    try {
        Connection con = DatabaseConnection.getConnection();
        String sql = "UPDATE recorrection SET status = ? WHERE recorrection_id = ?";
        PreparedStatement stmt = con.prepareStatement(sql);

        String selectedStatus = cmbStatus.getSelectedItem().toString(); // ComboBox for status
        int recorrectionId = Integer.parseInt(txtRecorrectionId.getText());

        stmt.setString(1, selectedStatus);
        stmt.setInt(2, recorrectionId);

        int result = stmt.executeUpdate();

        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Recorrection status updated successfully.");
            loadRecorrectionData(); // Refresh the JTable
        } else {
            JOptionPane.showMessageDialog(this, "Update failed. ID may be incorrect.");
        }

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error occurred while updating.");
    }        
        // TODO add your handling code here:
    }//GEN-LAST:event_btnChangeActionPerformed

