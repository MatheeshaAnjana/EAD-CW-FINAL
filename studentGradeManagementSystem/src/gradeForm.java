    private void loadStudentDetails(int studentId) {
    try {
        Connection con = DatabaseConnection.getConnection();
        String sql = "SELECT * FROM student WHERE student_id = ?";
        PreparedStatement stmt = con.prepareStatement(sql);
        stmt.setInt(1, studentId);
        ResultSet rs = stmt.executeQuery();

        if (rs.next()) {
            lblShowStID.setText(String.valueOf(rs.getInt("student_id")));
            lblShowtxtName.setText(rs.getString("student_name"));
            lblShowEmail.setText(rs.getString("email"));
            lblShowPhoneNo.setText(rs.getString("phone_number"));
        } else {
            lblShowStID.setText("---");
            lblShowtxtName.setText("---");
            lblShowEmail.setText("---");
            lblShowPhoneNo.setText("---");
        }

    } catch (Exception e) {
        e.printStackTrace();
    }
}
    private void loadSubjectDetails(int subjectId) {
    try {
        Connection con = DatabaseConnection.getConnection();
        String sql = "SELECT * FROM subject WHERE subject_id = ?";
        PreparedStatement stmt = con.prepareStatement(sql);
        stmt.setInt(1, subjectId);
        ResultSet rs = stmt.executeQuery();

        if (rs.next()) {
            lblShowSubjectId.setText(String.valueOf(rs.getInt("subject_id")));
            lblShowSubjectName.setText(rs.getString("subject_name"));
        } else {
            lblShowSubjectId.setText("---");
            lblShowSubjectName.setText("---");
        }

    } catch (Exception e) {
        e.printStackTrace();
    }
}


