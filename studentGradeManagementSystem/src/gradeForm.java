    private void loadResultData() {
    // Get the model from the table component (assumed to be pnlGradeDetail)
    DefaultTableModel model = (DefaultTableModel) pnlGradeDetails.getModel(); 

    // Set column headers (if not already defined)
    model.setColumnIdentifiers(new String[] {
        "Student ID", "Subject ID", "Marks", "Grade"
    });

    model.setRowCount(0); // Clear existing rows

    try {
        Connection con = DatabaseConnection.getConnection();
        String sql = "SELECT * FROM result_grade";
        Statement stmt = con.createStatement();
        ResultSet rs = stmt.executeQuery(sql);

        while (rs.next()) {
            String studentId = rs.getString("student_id");
            String subjectId = rs.getString("subject_id");
            String grade = rs.getString("grade");
            String marks = rs.getString("marks");

            model.addRow(new Object[]{studentId, subjectId, marks, grade});
        }

        // ✅ No need for: pnlReport.setmodel(model); — the table is already updated
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading result data: " + e.getMessage());
    }
}
