    private void btnUpdateGradeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateGradeActionPerformed
     if (!isFormValid()) return;

    try {
        Connection con = DatabaseConnection.getConnection();
        String sql = "UPDATE result_grade SET grade = ?, marks = ? WHERE student_id = ? AND subject_id = ?";
        PreparedStatement stmt = con.prepareStatement(sql);
        stmt.setString(1, cmbGrade.getSelectedItem().toString());
        stmt.setDouble(2, Double.parseDouble(txtMarksGrade.getText()));
        stmt.setInt(3, Integer.parseInt(txtStudentIdGrade1.getText()));
        stmt.setInt(4, Integer.parseInt(txtSubjectIdGrade.getText()));


        int result = stmt.executeUpdate();
        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Grade updated.");
            loadResultData();
        } else {
            JOptionPane.showMessageDialog(this, "No matching grade record found.");
        }
    } catch (Exception e) {
        e.printStackTrace();
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnUpdateGradeActionPerformed

    private void btnDeleteGrade1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteGrade1ActionPerformed
    if (!isFormValid()) return;

    int choice = JOptionPane.showConfirmDialog(this,
        "Are you sure you want to delete this grade?",
        "Confirm Delete",
        JOptionPane.YES_NO_OPTION);

    if (choice != JOptionPane.YES_OPTION) return;

    try {
        Connection con = DatabaseConnection.getConnection();
        String sql = "DELETE FROM result_grade WHERE student_id = ? AND subject_id = ?";
        PreparedStatement stmt = con.prepareStatement(sql);
        stmt.setInt(1, Integer.parseInt(txtStudentIdGrade1.getText()));
        stmt.setInt(2, Integer.parseInt(txtSubjectIdGrade.getText()));

        int result = stmt.executeUpdate();
        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Grade deleted.");
            loadResultData();
        } else {
            JOptionPane.showMessageDialog(this, "No grade record found to delete.");
        }
    } catch (Exception e) {
        e.printStackTrace();
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteGrade1ActionPerformed
    private boolean isFormValid() {
    if (txtStudentIdGrade1.getText().trim().isEmpty() ||
        txtSubjectIdGrade.getText().trim().isEmpty() ||
        cmbGrade.getSelectedItem() == null) {
        JOptionPane.showMessageDialog(this, "Please fill all fields.");
        return false;
    }
    return true;
}

